x = True
y = False
print('x and y :',x and y)
# x and y : False

print('x or y :',x or y)
# x or y : True

print('not x :',not x)
# not x : False

print('x is y :', x is y)
# x is y : False

print('x is not y :', x is not y)
# x is not y : True